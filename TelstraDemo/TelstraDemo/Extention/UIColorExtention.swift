import UIKit

extension UIColor {
    struct Blue {
        static let SkyBlue = _ColorLiteralType(red: 0.2431372549, green: 0.7647058824, blue: 0.8392156863, alpha: 1)
    }
    
    struct Black {
        static let BoldBlack = _ColorLiteralType(red: 0.2549019754, green: 0.2745098174, blue: 0.3019607961, alpha: 1)
    }
    
    struct White {
        static let CustomWhite = _ColorLiteralType(red: 1, green: 1, blue: 1, alpha: 1)
    }
   
}

